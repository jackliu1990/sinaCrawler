package crawler.sina.bean;

public class SinaUserInfo {

	private String uid;
	private String nick;
	
	public void setUid(String uid){
		this.uid=uid;
	}
	
	public String getUid(){
		return this.uid;
	}
	
	public void setNick(String nick){
		this.nick = nick;
	}
	
	public String getNick(){
		return this.nick;
	}
}
